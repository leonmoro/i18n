<h3>Escala</h3>
Juntamente com o cadastro de cultos e eventos, é possível cadastrar times, funções e integrantes no programa Holyrics, permitindo a criação de escalas para os cultos e eventos.<br>
O foco do programa não é a administração e organização de equipes, o programa disponibiliza apenas funções básicas de administração e criação de escalas.<br>
<b>Criar escalas baseadas em integrantes:</b> uma grade onde cada coluna é um culto ou evento, cada linha é um integrante e cada célula é uma caixinha pra definir se o integrante está ou não escalado para o respectivo culto/evento<br>
Nesse tipo de escala não é possível definir em qual função o integrante está escalado para exercer.<br>
<b>Criar escalas baseadas em funções:</b> uma grade onde cada coluna é um culto ou evento, cada linha é uma função e cada célula é uma lista de integrantes que podem ser escalados para uma função<br>

