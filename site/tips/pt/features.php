<h3>Cultos e eventos</h3>
<ul>
    <li>Cadastrar cultos e eventos regulares no sistema</li>
    <li>Cadastrar um evento específico, definindo a data e hora do evento</li>
    <li>
        Os cultos cadastrados geram automaticamente as listas de reprodução de cada culto.<br>
        Por exemplo, um culto cadastrado como "todo domingo, 19h", terá uma lista de reprodução criada automaticamente para todos os domingos às 19h.<br>
        Permitindo cadastrar as músicas e mídias utilizadas em cada culto e evento de forma independente.
    </li>
    <li>
        Configurar um <a href='#wallpaper'>papel de parede</a> para um culto (menu arquivo, configurações, papel de parede)<br>
        Configurar um <a href='#wallpaper'>papel de parede</a> para um evento (no popup de edição de evento)<br>
        Dessa forma, o <a href='#wallpaper'>papel de parede</a> utilizado na projeção será automaticamente a imagem definida para o respectivo culto ou evento da lista de reprodução selecionada na janela principal do programa.<br>
        Evitando ter que trocar de papel de parede todo culto ou evento diferente.
    </li>
    <li>
        Ao cadastrar um culto com a regra "todos", é possível ocultar algumas semanas específicas<br>
        Por exemplo, você deseja cadastrar um culto de Santa Ceia "todo segundo domingo"<br>
        Se você tiver um culto cadastrado como "todo domingo", uma lista de reprodução duplicada poderá ser gerada<br>
        Então ao ocultar a semana 2 do culto "todo domingo", a lista duplicada é evitada, gerando apenas a lista de reprodução do culto "Santa Ceia" para o segundo domingo de cada mês.
    </li>
</ul>

