<h3>Imagem, áudio, vídeo e arquivos</h3>
O programa Holyrics trabalha de forma integrada ao programa VLC Player para execução de áudio e vídeo.<br>

<ul>
    <li>Biblioteca de áudio, vídeo e imagem na janela principal do programa, para organização de arquivos</li>
    <li>Pesquisar arquivos na pasta (e em subpastas, conforme configuração)</li>
    <li>
        Ao criar uma subpasta, ela vira uma lista de reprodução para exibir as mídias dentro dessa subpasta.<br>
        Ou seja, para exibir uma sequência de imagens, crie uma subpastas e adicione todas as imagens dentro da respectiva subpasta.<br>
    </li>
    <li>Apresentação automática de imagens (botão direito do mouse em cima de uma subpasta)</li>
    <li>Compatível com arrastar e soltar arquivos</li>
    <li>Adicionar atalhos para listar arquivos de outras pastas do computador</li>
    <li>Definir ajuste para exibição de vídeo e imagem: preencher, ajustar, ...</li>
    <li>Criar vídeo a partir de uma sequência de imagens (botão direito do mouse em uma subpasta com imagens)</li>
    <li>Aplicar efeitos em uma imagem (brilho, contraste, matiz, etc)</li>
    <li>Exibir imagem com efeito blur quando a imagem for exibida fora da proporção da tela de projeção</li>
    <li>Exibir diferentes imagens (resoluções diferentes, por exemplo) em diferentes telas (<a href='/tips/image_multiple_screens.html' target='_blank'>Saiba mais</a>)</li>
    <li>
        Edição de mídia (botão direito do mouse em uma mídia - editar)<br>
        Áudio - recortar trecho<br>
        Vídeo - recortar trecho / alterar resolução e bitrate / extrair áudio<br>
        Imagem - recortar / redimensionar
    </li>
    <li>
        Opção para renomear o arquivo (botão direito do mouse) diretamente pelo programa em vez de renomear pelo explorador de arquivos do sistema operacional<br>
        Isso permite que as referências ao arquivo utilizadas nos recursos do programa (barra de favoritos, por exemplo) sejam atualizadas ao renomear o arquivo.<br>
        Ao renomear pelo explorador de arquivos, as referências nos recursos do programa são perdidas, tentando acessar e executar o arquivo com o nome anterior.
    </li>
    <li>
        Na aba arquivos é possível adicionar e organizar qualquer tipo de arquivo, porém os arquivos, em geral, serão abertos pelo próprio sistema operacional.<br>
        O programa Holyrics pode executar arquivos .ppt .pptx dessa pasta, porém disponível apenas para Windows, e é necessário ter o programa PowerPoint instalado.
    </li>
</ul>

