<h3>Lista de reprodução</h3>
Todos os cultos, eventos regulares e eventos únicos cadastrados no sistema geram uma lista de reprodução independente, permitindo organizar a lista de reprodução de cada culto e evento com antecedência, e mantendo um histórico de todas as programações feitas anteriormente.
<ul>
    <li>
        Lista de reprodução de letra de música<br>
        Adicionar todas as letras de música que serão utilizadas no respectivo culto ou evento.<br>
        É possível selecionar múltiplos itens para executar em sequência<br>
        Opção nas configurações para salvar um histórico de exibição automaticamente (marcar como tocada) ao exibir uma letra de música e ela estiver na lista de reprodução
    </li>
    <li>
        Lista de reprodução de mídia (também aceita letra de música)<br>
        Mídia nesse caso é um termo genérico, não significa apenas áudio, vídeo e imagem<br>
        A lista de reprodução de mídia é o local para adicionar e organizar todos os itens que serão executados no respectivo culto ou evento.<br>
        Letra de música, versículos da Bíblia, texto, áudio, vídeo, imagem, anúncio, apresentação automática, contagem regressiva, etc.<br>
        É possível organizar a lista com títulos, que podem ocultar ou expandir os subitens adicionados abaixo do respectivo título (<a href='/tips/playlist_title.html' target='_blank'>Saiba mais</a>).<br>
        Definir duração para cada item da lista<br>
        Exibir o provável horário de execução do item (horário da programação) a partir do início do culto/evento e a duração de cada item anterior na lista (linha do tempo)
    </li>
    <li>Compartilhar online uma lista de letra de música, podendo ser acessada pelo navegador e também baixada em outro computador</li>
    <li>Salvar uma lista de reprodução em arquivo, para abrir em outro computador</li>
    <li>Salvar modelos de lista de mídia para reutilização em outros cultos e eventos</li>
</ul>

