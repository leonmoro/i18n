<h2>Lista dos principais recursos do programa Holyrics</h2>

<h3>Projeção</h3>
O programa Holyrics é um software de projeção, controlando e exibindo apresentações na tela secundária do sistema e também as demais telas para informações extras.<br>
Suporta apresentações de:
<ul>
    <li>Letra de música</li>
    <li>Versículos da Bíblia</li>
    <li>Textos diversos</li>
    <li>Imagem, áudio e vídeo</li>
    <li>Contagem regressiva</li>
    <li>Informação de alerta (no rodapé ou topo da tela de projeção)</li>
    <li>Texto na tela de retorno (visão do palco)</li>
    <li>Contagem regressiva ou cronômetro na tela de retorno</li>
    <li>Informação de alerta na tela de retorno</li>
</ul>

<hr />

<h3>Independência entre conteúdo e layout</h3>
Holyrics trabalha com independência entre um texto digitado que será exibido na tela, e a formatação, layout e plano de fundo que serão utilizados para exibir esse conteúdo na tela.<br>
Ao digitar uma letra de música, um texto para exibição, ou selecionar um versículo para exibição na tela, sempre haverá um Tema associado criado pelo usuário com as informações de nome da fonte, tamanho da fonte, cor, plano de fundo, alinhamento, etc. para exibir este conteúdo.<br>
Ou seja, não é necessário aplicar formatações slide por slide, como necessário em programas tipo PowerPoint.<br>
Basta apenas selecionar o texto para exibição e ele será exibido com a formatação, layout e plano de fundo de acordo com o Tema definido.<br>

