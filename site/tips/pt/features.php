<h3>Tema</h3>
Para exibir um conteúdo na projeção, o programa utiliza Temas para aplicar a formatação de exibição.<br>
É possível criar diversos Temas para uso nos diferentes recursos do programa, e facilmente alternar entre os Temas criados com apenas um clique.<br>
Configurações disponíveis para um tema:<br>
<ul>
    <li>Plano de fundo: cor sólida, imagem, vídeo, transparente</li>
    <li>Fonte, tamanho, cor, contorno, brilho, sombra, negrito, itálico</li>
    <li>Margem, alinhamento vertical e horizontal, espaçamento entre linhas e caracteres</li>
    <li>Cor de fundo e contorno - aplique uma cor de fundo em cada linha </li>
    <li>
        Texturas - é possível utilizar texturas como "cor" da fonte e cor de fundo (<a href='/tips/color_texture.html' target='_blank'>Saiba mais</a>)<br>
        • Cor sólida: utilizar uma cor sólida padrão<br>
        • Gradiente: utilizar uma combinação de 2 ou mais cores, formando um gradiente horizontal ou vertical<br>
        • Imagem: utilizar uma imagem como textura para renderização<br>
        • Transparente: exibir a fonte de forma transparente, em combinação com a cor de fundo, dando um efeito vazado, exibindo o plano de fundo atrás do texto
    </li>
    <li>Comentário: configurações de formatação para comentários</li>
    <li>Configurações diversas: renderizar o texto em maiúsculo, definir quebra de linha automática</li>
    <li>Organizar temas por tags, por exemplo: azul, água, círculo, etc.</li>
</ul>
<b>Observação:</b> Por padrão, vídeos utilizados como plano de fundo são executados apenas na tela público, por questão de desempenho.<br>
As demais telas do sistema utilizarão uma imagem estática no lugar do vídeo de plano de fundo.<br>
É possível definir uma tela adicional para exibir vídeo como plano de fundo no menu arquivo, configurações, aba avançado.

