Товар будет выполнен в указанный день и время.<br>
Для некоторых запланированных элементов, например видео, можно определить дополнительные настройки, указав громкость, которая будет установлена ​​в плеере при воспроизведении видео.<br>
За 1 минуту до времени выполнения элемента в углу экрана отображается уведомление о том, что элемент скоро будет выполнен, с кнопкой отмены действия при необходимости.<br>
Примечание. Это внутренняя функция программы, она работает только в том случае, если программа открыта.<br>
Вы также можете запланировать запуск задачи один раз в определенное время, щелкнув элемент правой кнопкой мыши.<br>
Например, щелкнув правой кнопкой мыши изображение и запланировав запуск задачи в определенное время.<br />

