<h2>Список основных функций программы Holyrics</h2>

<h3>Проекция</h3>
Программа Holyrics — это проекционное программное обеспечение, отображающее и управляющее презентациями на дополнительном экране системы, а также на других экранах для представления дополнительной информации.<br>
<br>
Поддерживаются:<br>
<ul>
  <li>Тексты песен.</li>
  <li>Библейские стихи.</li>
  <li>Произвольные тексты.</li>
  <li>Изображения, аудио и видео.</li>
  <li>Обратный отсчет.</li>
  <li>Предупреждения бегущей строкой (внизу или вверху проекционного экрана).</li>
  <li>Текст на мониторе сцены.</li>
  <li>Обратный отсчет или таймер на мониторе сцены.</li>
  <li>Оповещения на мониторе сцены.</li>
</ul>

<hr />

<h3>Независимость между контентом и макетом</h3>
Holyrics работает независимо между набранным текстом, который будет отображаться на экране, и форматированием, макетом и фоном, которые будут использоваться для отображения этого контента на экране.<br>
При вводе или выборе текста, на экране всегда будет применена связанная тема для отображения этого контента с информацией о названии шрифта, размере шрифта, цвете, фоне, выравнивании и т. д.<br>
Другими словами, нет необходимости применять форматирование к каждому слайду, как в программах типа PowerPoint.<br>
Просто выберите текст для отображения, и он будет отображаться с форматированием, макетом и фоном в соответствии с заданной темой.<br>

