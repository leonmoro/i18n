<ul>
  <li>Изменение темы/форматирования/макета, используемых для отображения контента на мониторе сцены.</li>
  <li>Отображение части следующего куплета/припева песни на мониторе сцены.</li>
  <li>Различное форматирование для текущего содержимого и содержимого предстоящих слайдов.</li>
  <li>Отображение комментариев к отображаемому контенту.</li>
  <li>Отображение содержимого <a href='#communication_panel'>панели связи</a>.</li>
  <li>Отображение следующего изображения при отображении списка изображений.</li>
  <li>Выбор типа контента, отображаемого для вывода через http.</li>
</ul>

