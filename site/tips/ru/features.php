<h3>Изображения, аудио, видео и файлы</h3>

Программа Holyrics интегрирована с программой VLC Player для воспроизведения аудио и видео.<br>

<ul>
  <li>Библиотека аудио, видео и изображений в главном окне программы для организации файлов.</li>
  <li>Поиск файлов в папке (и подпапках, в зависимости от конфигурации).</li>
  <li>Отображение папки как списка воспроизведения. Когда вы создаете подпапку и добавляете в нее файлы, она становится списком воспроизведения медиафайлов в этой подпапке.</li>
  <li>Автоматическое отображение изображений (правая кнопка мыши на подпапке).</li>
  <li>Поддержка перетаскивания файлов.</li>
  <li>Ярлыки для отображения файлов из других папок на вашем компьютере.</li>
  <li>Настройка размера отображения видео и изображений: заполнить, подогнать...</li>
  <li>Создание видео из последовательности изображений (щелкните правой кнопкой мыши подпапку с изображениями).</li>
  <li>Применение к изображению эффектов (яркость, контрастность, оттенок и т. д.).</li>
  <li>Отображение изображения с эффектом размытия, когда изображение выходит за пределы проекционного экрана.</li>
  <li>Отображение разных изображений (например, разного разрешения) на разных экранах (<a href='/tips/image_multiple_screens.html' target='_blank'>Подробнее</a>).</li>
  <li>Редактирование медиа (щелкните правой кнопкой мыши по медиа - редактировать):<br>
    • Аудио – назначение начала и конца воспроизведения.<br>
    • Видео - назначение начала и конца воспроизведения, изменение разрешения и битрейта, извлечение аудио.<br>
    • Изображение – обрезка, изменение размера.</li>
  <li>Возможность переименовать файл непосредственно из Holyrics (правая кнопка мыши).<br> При этом, ссылки на файлы, используемые в функциях программы (например, панель закладок) корректно обновляются, тогда как при переименовании через файловый менеджер операционной системы эти ссылки перестают работать.</li>
  <li>На вкладке «Файлы» вы можете добавлять и упорядочивать файлы любого типа, но файлы, как правило, будут открываться самой операционной системой.<br> Программа Holyrics может запускать файлы .ppt .pptx из этой папки, но это доступно только для Windows, и для этого необходимо установить программу PowerPoint.</li>
</ul>

