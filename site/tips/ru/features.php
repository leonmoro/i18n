  <li>Отображение версии Библии в проекции в полном или сокращенном виде, или с учетом авторских прав, индивидуально для каждой версии Библии.<br> Некоторые правообладатели требуют отображения полного названия перевода и/или информации об авторских правах, при этом в проекции не допускается отображать только аббревиатуру версии.<br> Это зависит не от программы Holyrics.</li>
</ul>

