<h3>Заказать музыкальные слайды как сборник гимнов</h3>
Возможность автоматического добавления слайда припева/хора между каждым куплетом песни в презентации.<br />
Создать или отредактировать группу песен (меню редактирования, группу);<br />
Щелкните правой кнопкой мыши по нужной группе и активируйте настройку;<br />
Все песни в этой группе будут отображаться с добавлением припева или слайда припева поочередно с другими слайдами (куплетами) песни.<br />
Примечание. В тексте песни должно быть как минимум описание слайда «припев» или «припев», добавленное на правильный слайд, чтобы определить, какой слайд будет добавлен среди других слайдов (куплетов)<br />

