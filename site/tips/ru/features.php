<h3>Автоматический показ</h3>
(меню инструменты)<br />
Возможность создать автоматическую презентацию текста песни, задав длительность каждого слайда, в соответствии с выбранным музыкальным сопровождением в формате MP3.<br>
При запуске такой презентации слайды с текстами песен будут воспроизводиться одновременно со звуком и переключаться автоматически.<br>

