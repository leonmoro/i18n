<h3>Журнал</h3>
В программе можно создавать и просматривать отчеты об использовании.<br>
<ul>
  <li>
<b>Воспроизведенные песни:</b> отчет со списком текстов песен, наиболее часто отображаемых в проекции.<br>
Пользователь должен установить метку «воспроизведенной песни» при отображении музыки в проекции или включить автоматическую пометку в настройках списка воспроизведения.</li>
  <li>
<b>Расписание:</b> список, в котором указано, сколько раз был запланирован каждый участник или песня.</li>
  <li>
<b>Журнал активности:</b> отчет обо всех презентациях, сделанных в программе (тексты песен, медиа и т. д.).<br>
Необходимо активировать ведение журнала в настройках.</li>
</ul>

