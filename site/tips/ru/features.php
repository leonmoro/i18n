<h3>Персонализированное сообщение</h3>
Функционал для создания предварительно отформатированных сообщений для более практичного и организованного отображения многоразового текстового сообщения.<br>
Например: <i>{имя</i>, пожалуйста, зайдите в <i>{location}</i>.<br>
Где <i>{имя</i> и <i>{местоположение</i> — это поля, которые будут заполнены пользователем без необходимости записывать весь контент.<br>
Сообщение может отображаться на общем экране или на мониторе сцены, и его можно отображать в полноэкранном режиме или в виде оповещения.<br>

