<h3>Служения и мероприятия</h3>
<ul>
  <li>Регистрация в системе регулярных служений и мероприятий.</li>
  <li>Регистрация разовых мероприятий с указанием их даты и времени.</li>
  <li>Для зарегистрированных служений автоматически создаются плейлисты.<br> В каждый из них заранее можно добавить песни, тексты или медиафайлы.</li>
  <li>Настройка <a href='#wallpaper'>обоев</a> для служения (файл меню, настройки, обои).<br> Настройка <a href='#wallpaper'>обоев</a> для мероприятия (окно редактирования мероприятия).<br> Таким образом, в проекции всегда будут автоматически использоваться <a href='#wallpaper'>обои</a>, заранее определенные для соответствующего служения или мероприятия из списка воспроизведения, выбранного в главном окне программы.</li>
  <li>При регистрации служений с правилом «Все» есть возможность скрыть отдельные недели.<br> Например, если вы хотите зарегистрировать причастие на «Каждое первое воскресенье», и у вас есть служение, зарегистрированное на «Каждое воскресенье», то чтобы избежать дублирования плейлиста, нужно исключить «Первую неделю» для служения «Каждое воскресенье».</li>
</ul>

