<h3>Плейлист</h3>
Все служения, регулярные и уникальные мероприятия, зарегистрированные в системе, формируют независимый плейлист, что позволяет заранее организовать плейлист для каждого служения и мероприятия, а также хранить историю всех ранее созданных.
<ul>
  <li>Плейлист Песни.<br>
Добавление текстов песен, которые будут использоваться в соответствующем служении или мероприятии.<br>
Можно выбрать несколько элементов для последовательного отображения.<br>
Опция в настройках для автоматического сохранения истории (отметить как прослушанную) при просмотре текстов песен и их включения в список воспроизведения.</li>
  <li>Плейлист Медиа (также поддерживает тексты песен).<br>
Медиа в данном случае — это общий термин, он не означает просто аудио, видео и изображение.<br>
Медиа-плейлист – это место для добавления и систематизации всех элементов, которые будут воспроизводиться на соответствующей служении или мероприятии.<br>
Тексты песен, библейские стихи, текст, аудио, видео, изображения, объявления, автоматическое представление, обратный отсчет и т. д.<br>
Возможность организовать список с помощью заголовков, которые могут скрывать или расширять подэлементы, добавленные под соответствующим заголовком (<a href='/tips/playlist_title.html' target='_blank'>Подробнее</a>).<br>
Установка продолжительности для каждого элемента списка.<br>
Отображение вероятного времени выполнения элемента (время расписания) от начала служения/мероприятия и продолжительности каждого предыдущего элемента в списке (временная шкала).</li>
  <li>Делитесь в Интернете списком текстов песен, к которому можно получить доступ через браузер, а также загрузить на другой компьютер.</li>
  <li>Сохранение плейлиста в файл для открытия на другом компьютере.</li>
  <li>Сохранять шаблоны списков мультимедиа для повторного использования в других служениях и мероприятиях.</li>
</ul>

