<h3>[Описание слайда] Группировать похожие описания</h3>
В некоторых случаях слайды с текстами песен могут иметь разные описания, но означать одно и то же.<br>
Например: хор и хор.<br>
Чтобы гарантировать, что описание припева всегда идентифицируется программой как описание припева:<br>
- Доступ к настройкам описания слайдов (настройки отображения);<br>
- Нажмите на 3 точки в описании хора;<br>
– Выберите псевдонимы > добавьте и добавьте название припева.<br>

