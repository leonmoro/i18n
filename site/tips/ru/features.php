<h3>Темы</h3>
Для отображения форматированного контента в проекции программа использует темы.<br> Можно создать несколько тем для использования в различных функциях программы и легко переключаться между ними одним щелчком мыши.<br>
<br>
Доступные настройки тем:<br>
<ul>
  <li>Фон: сплошной цвет, изображение, видео, прозрачный.</li>
  <li>Шрифт: размер, цвет, контур, яркость, тень, жирный шрифт, курсив.</li>
  <li>Поля, вертикальное и горизонтальное выравнивание, расстояние между строками и символами.</li>
  <li>Цвет фона и контур применительно к каждой строке.</li>
  <li>Текстуры можно использовать в качестве цвета для шрифта и фона 
    (<a href='/tips/color_texture.html' target='_blank'>Подробнее</a>):<br>
    • Сплошной цвет.<br>
    • Градиент.<br>
    • Изображение.<br>
    • Прозрачный: видны только контур и тень шрифта.</li>
  <li>Комментарий: настройки форматирования комментариев.</li>
  <li>Различные настройки: отображение текста заглавными буквами, установка автоматического переноса строк.</li>
  <li>Упорядочивание тем по тегам, например: синий, вода, круг и т. д.</li>
</ul>
<b>Примечание:</b><br>
По умолчанию, видео, используемые в качестве фона, воспроизводятся только на общедоступном экране из соображений производительности.<br>
На других экранах системы вместо фонового видео будет использоваться статическое изображение.<br>
В меню «Файл», Настройки, вкладка «Дополнительно» можно установить дополнительный экран для отображения видео в качестве фона.

