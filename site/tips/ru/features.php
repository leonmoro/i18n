<h3>Расширенный редактор</h3>
(окно редактирования, 3 точки)<br />
Расширенное окно редактирования текстов песен и текстового представления.<br>
Позволяет вносить расширенные изменения в слайд, применяя определенное форматирование к словам или отрывкам на слайде.<br>
Шрифт, размер, жирный шрифт, курсив, подчеркивание и т. д.<br>
